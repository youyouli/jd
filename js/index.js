function run1(name){
	var Main1=document.getElementsByClassName(name)[0];
	var Aside=Main1.getElementsByClassName('aside')[0];
	var Pupop=Main1.getElementsByClassName('pupop')[0];
	var Ul=Aside.getElementsByTagName('ul')[0];
	var A=Ul.getElementsByTagName('a');
	var Pupop=Aside.getElementsByClassName('pupop');
	for(var i=0;i<A.length;i++){
			A[i].index=i
			A[i].onmouseover=function(){
				for(var j=0;j<Pupop.length;j++){
					Pupop[j].style.display='none'	
				}
				Pupop[this.index].style.display='block'
			}	
			A[i].onmouseout=function(){
					for(var j=0;j<Pupop.length;j++){
						Pupop[j].style.display='none'	
					}
			}
			Pupop[i].onmouseover=function(){
				this.style.display='block'	
			}	
			Pupop[i].onmouseout=function(){
					this.style.display='none'	
			}	
	}
		
}
