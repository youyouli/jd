function run2(){
	var arr=['服饰','美妆','手机','家电','数码','运动','居家','母婴','食品','图书','车品','服务']
	var Main=document.getElementsByClassName('main')[2];
	var Floor=document.getElementsByClassName('floor_nav')[0];
	var main=document.getElementsByClassName('main');
	var P=Floor.getElementsByTagName('p');
	window.onscroll=function(){
		var dis_scroll=document.documentElement.scrollTop||document.body.scrollTop
		if(dis_scroll>main[3].offsetTop-document.documentElement.clientHeight/2
			&&
			dis_scroll<main[14].offsetTop+main[14].offsetHeight-document.documentElement.clientHeight/2
			){
			Floor.style.display='block'
			Floor.style.left=(Main.offsetLeft-Floor.offsetWidth)+'px';
		}
		else if(dis_scroll>main[14].offsetTop+main[14].offsetHeight-document.documentElement.clientHeight/2
				||
				dis_scroll<main[3].offsetTop-document.documentElement.clientHeight/2
				){
			Floor.style.display='none'
		}
		for(var i=0;i<P.length;i++){
			if(dis_scroll>main[i+3].offsetTop-document.documentElement.clientHeight/2
				&&
				dis_scroll<main[i+3].offsetTop+main[i+3].offsetHeight-document.documentElement.clientHeight/2){
					P[i].style.color='red'
					P[i].innerHTML=arr[i]	
			}
			if(dis_scroll>main[i+3].offsetTop+main[i+3].offsetHeight-document.documentElement.clientHeight/2
			 	||
				dis_scroll<main[i+3].offsetTop-document.documentElement.clientHeight/2){
					P[i].style.color='#A6A6A6'
					P[i].innerHTML=i+1+'F'	
			}
		}		

	}
	for(var j=0;j<P.length;j++){
		P[j].index=j
		P[j].onclick=function(){
			document.documentElement.scrollTop=main[this.index+3].offsetTop //火狐
			document.body.scrollTop=main[this.index+3].offsetTop//谷歌
		}	
	}
}
